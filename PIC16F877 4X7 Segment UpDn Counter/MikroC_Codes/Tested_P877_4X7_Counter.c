

//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
char DIGIT0,DIGIT1,DIGIT2,DIGIT3;
char DSP_code[10]= {0x3F,0x06,0x5B,0x4F,0x66,0x6D,0x7D,0x07,0x7F,0x6F};
//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||



//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
void main(){
int i;   //by dfault unsigned and 0 - 65535 (2 byte Unsigned)
//ANSELH = 0;
TRISB = 0;
PORTB = 0;
TRISA = 0;
PORTA = 0;
TRISD = 0;
PORTD = 0;

DIGIT0 = 0xFE;   // 0b11111110
DIGIT1 = 0xFD;   // 0b11111101
DIGIT2 = 0xFB;
DIGIT3 = 0xF7;


//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
while(1){
for(i=0; i<9999; i++){
//=============Convert and Display the ones digit====================///////////
PORTD = DIGIT0;  // RA.0 to RA3 Output
PORTB = DSP_code[i%10u];
delay_ms(10);
//=============Convert and Display the tens digit====================///////////
PORTD = DIGIT1;  // RA.0 to RA3 Output
PORTB = DSP_code[(i/10)%10u];
delay_ms(10);
//=============Convert and Display the hundred digit====================////////
PORTD = DIGIT2;  // RA.0 to RA3 Output
PORTB = DSP_code[(i/100)%10u];
delay_ms(10);
//=============Convert and Display the thousand digit====================///////
PORTD = DIGIT3;  // RA.0 to RA3 Output
//PORTB = DSP_code[i==0];
PORTB = DSP_code[(i/1000)%10u];
delay_ms(10);
PORTB = 0;
//||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
delay_ms(10);
}
}
} // end main
